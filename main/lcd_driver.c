// 新增：获取字符串宽度（用于布局计算）
uint16_t lcd_get_string_width(lcd_display_t *lcd, const char *str)
{
    if (lcd == NULL || str == NULL || lcd->current_font == NULL) {
        return 0;
    }
    
    uint16_t width = 0;
    while (*str) {
        if (*str == '\n' || *str == '\r') {
            break;
        }
        width += lcd->current_font->width + 1;
        str++;
    }
    
    return width > 0 ? width - 1 : 0; // 减去最后一个字符的间距
}

